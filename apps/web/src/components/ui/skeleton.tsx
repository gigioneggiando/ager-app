import { cn } from "@/lib/utils";

/** Loading placeholder. Uses the beige muted surface with a subtle pulse. */
function Skeleton({
  className,
  ...props
}: React.HTMLAttributes<HTMLDivElement>) {
  return (
    <div
      className={cn("animate-pulse rounded-md bg-muted", className)}
      {...props}
    />
  );
}

export { Skeleton };
